#include <iostream>

using namespace std;

int main(){
    int x = 10;
    int y = 20;
    int z = 30;

    //Finding the sum
    int sum;
    sum = x + y + z;
    cout << sum << endl;

    //Finding the average
    int average;
    average = (x + y + z) / 3;
    cout << average << endl;
}